std::string EncodingDetector::uint32_to_utf8(uint32_t cp) noexcept {
    std::string out;
    if (cp < 0x80) {
        out += static_cast<char>(cp);
    } else if (cp < 0x800) {
        out += static_cast<char>(0xC0 | (cp >> 6));
        out += static_cast<char>(0x80 | (cp & 0x3F));
    } else if (cp < 0x10000) {
        out += static_cast<char>(0xE0 | (cp >> 12));
        out += static_cast<char>(0x80 | ((cp >> 6) & 0x3F));
        out += static_cast<char>(0x80 | (cp & 0x3F));
    } else if (cp <= 0x10FFFF) {
        out += static_cast<char>(0xF0 | (cp >> 18));
        out += static_cast<char>(0x80 | ((cp >> 12) & 0x3F));
        out += static_cast<char>(0x80 | ((cp >> 6)  & 0x3F));
        out += static_cast<char>(0x80 | (cp & 0x3F));
    } else {
        out += '\xEF'; out += '\xBF'; out += '\xBD'; // U+FFFD
    }
    return out;
}

// ---------------------------------------------------------------------------
float EncodingDetector::compute_utf8_validity_score(std::span<const uint8_t> data) const noexcept {
    UTF8Scanner scanner;
    size_t valid_seqs = 0, total_seqs = 0;
    const uint8_t* ptr = data.data();
    const uint8_t* end = ptr + std::min(data.size(), size_t(8192));

    while (ptr < end) {
        ++total_seqs;
        uint8_t b = *ptr;
        if (b < 0x80) { ++valid_seqs; ++ptr; continue; }
        if (b < 0xC2) { ++ptr; continue; } // invalid lead
        if (b < 0xE0) {
            if (ptr+1 < end && (ptr[1] & 0xC0) == 0x80) { ++valid_seqs; ptr += 2; } else ++ptr;
        } else if (b < 0xF0) {
            if (ptr+2 < end && (ptr[1]&0xC0)==0x80 && (ptr[2]&0xC0)==0x80) { ++valid_seqs; ptr+=3; } else ++ptr;
        } else {
            if (ptr+3 < end && (ptr[1]&0xC0)==0x80 && (ptr[2]&0xC0)==0x80 && (ptr[3]&0xC0)==0x80) { ++valid_seqs; ptr+=4; } else ++ptr;
        }
    }
    return total_seqs > 0 ? static_cast<float>(valid_seqs) / total_seqs : 0.0f;
}

float EncodingDetector::compute_tcvn3_score(std::span<const uint8_t> data) const noexcept {
    // TCVN3: high bytes cluster in 0x80-0xEF range with specific distribution
    uint32_t total = 0, tcvn3_like = 0, high_bytes = 0;
    size_t n = std::min(data.size(), size_t(8192));
    for (size_t i = 0; i < n; ++i) {
        ++total;
        uint8_t b = data[i];
        if (b >= 0x80) {
            ++high_bytes;
            // TCVN3 Vietnamese chars are in 0x80-0xDF range specifically
            if (b >= 0x80 && b <= 0xDF && TCVN3_TO_UNICODE[b] != b) ++tcvn3_like;
        }
    }
    if (total == 0 || high_bytes == 0) return 0.0f;
    float ratio = static_cast<float>(high_bytes) / total;
    float viet_ratio = static_cast<float>(tcvn3_like) / high_bytes;
    return ratio > TCVN3_HIGH_BYTE_RATIO ? viet_ratio * 0.8f : 0.0f;
}

float EncodingDetector::compute_vni_score(std::span<const uint8_t> data) const noexcept {
    uint32_t total = 0, vni_like = 0, high = 0;
    size_t n = std::min(data.size(), size_t(8192));
    for (size_t i = 0; i < n; ++i) {
        ++total;
        uint8_t b = data[i];
        if (b >= 0x80) {
            ++high;
            if (VNI_TO_UNICODE[b] != b && VNI_TO_UNICODE[b] != 0xFFFD) ++vni_like;
        }
    }
    if (total == 0 || high == 0) return 0.0f;
    float ratio = static_cast<float>(high) / total;
    float viet  = static_cast<float>(vni_like) / high;
    return ratio > VNI_HIGH_BYTE_RATIO ? viet * 0.75f : 0.0f;
}

float EncodingDetector::compute_win1258_score(std::span<const uint8_t> data) const noexcept {
    uint32_t total = 0, hit = 0;
    size_t n = std::min(data.size(), size_t(8192));
    for (size_t i = 0; i < n; ++i) {
        ++total;
        uint8_t b = data[i];
        if (b >= 0x80) {
            uint32_t mapped = WIN1258_TO_UNICODE[b];
            if (mapped != 0xFFFD && mapped != (uint32_t)b) ++hit;
        }
    }
    return total > 0 ? static_cast<float>(hit) / total * 0.7f : 0.0f;
}

// ---------------------------------------------------------------------------
EncodingDetectionResult EncodingDetector::detect(std::span<const uint8_t> data) const noexcept {
    EncodingDetectionResult result{};

    if (data.empty()) {
        result.detected = Encoding::ASCII;
        result.confidence = 1.0f;
        result.charset_name = "us-ascii";
        result.message = "Empty file";
        return result;
    }

    // Check BOM
    if (data.size() >= 3 && data[0] == 0xEF && data[1] == 0xBB && data[2] == 0xBF) {
        result.detected = Encoding::UTF8_BOM;
        result.confidence = 0.999f;
        result.charset_name = "utf-8-sig";
        result.has_bom = true;
        result.needs_conversion = false;
        result.message = "UTF-8 with BOM detected";
        return result;
    }

    // Check pure ASCII
    bool all_ascii = true;
    for (size_t i = 0; i < std::min(data.size(), size_t(8192)); ++i) {
        if (data[i] >= 0x80) { all_ascii = false; break; }
    }
    if (all_ascii) {
        result.detected = Encoding::ASCII;
        result.confidence = 0.999f;
        result.charset_name = "us-ascii";
        result.needs_conversion = false;
        result.message = "Pure ASCII";
        return result;
    }

    // Score each encoding
    float utf8_score   = compute_utf8_validity_score(data);
    float tcvn3_score  = compute_tcvn3_score(data);
    float vni_score    = compute_vni_score(data);
    float win_score    = compute_win1258_score(data);

    // UTF-8 wins if near-perfect validity
    if (utf8_score >= UTF8_MIN_VALIDITY) {
        result.detected = Encoding::UTF8;
        result.confidence = utf8_score;
        result.charset_name = "utf-8";
        result.needs_conversion = false;
        result.message = "UTF-8 detected (score=" + std::to_string(utf8_score) + ")";
        return result;
    }

    // Compare legacy encodings
    struct Candidate { Encoding enc; float score; const char* name; };
    Candidate candidates[] = {
        {Encoding::TCVN3,       tcvn3_score, "tcvn3"},
        {Encoding::VNI,         vni_score,   "vni"},
        {Encoding::Windows1258, win_score,   "windows-1258"},
        {Encoding::UTF8,        utf8_score,  "utf-8"},
    };

    auto best = std::max_element(std::begin(candidates), std::end(candidates),
        [](const Candidate& a, const Candidate& b){ return a.score < b.score; });

    result.detected = best->enc;
    result.confidence = best->score;
    result.charset_name = best->name;
    result.needs_conversion = (best->enc != Encoding::UTF8);
    result.message = "Detected " + std::string(best->name) +
                     " (score=" + std::to_string(best->score) + ")";
    return result;
}

// ---------------------------------------------------------------------------
std::expected<std::string, std::string>
EncodingDetector::remap_to_utf8(std::span<const uint8_t> data,
                                  const uint32_t* table) const noexcept {
    std::string out;
    out.reserve(data.size() * 2); // Vietnamese expansion ~1.5x
    for (uint8_t b : data) {
        uint32_t cp = table[b];
        if (cp == 0xFFFD) {
            out += '\xEF'; out += '\xBF'; out += '\xBD'; // replacement char
        } else {
            out += uint32_to_utf8(cp);
        }
    }
    return out;
}

std::expected<std::string, std::string>
EncodingDetector::tcvn3_to_utf8(std::span<const uint8_t> data) const noexcept {
    return remap_to_utf8(data, TCVN3_TO_UNICODE);
}

std::expected<std::string, std::string>
EncodingDetector::vni_to_utf8(std::span<const uint8_t> data) const noexcept {
    return remap_to_utf8(data, VNI_TO_UNICODE);
}

std::expected<std::string, std::string>
EncodingDetector::win1258_to_utf8(std::span<const uint8_t> data) const noexcept {
    return remap_to_utf8(data, WIN1258_TO_UNICODE);
}

std::expected<std::string, std::string>
EncodingDetector::normalize_to_utf8(std::span<const uint8_t> data,
                                      const EncodingDetectionResult& enc) const noexcept {
    switch (enc.detected) {
        case Encoding::UTF8:
        case Encoding::UTF8_BOM:
        case Encoding::ASCII: {
            // Strip BOM if present
            size_t start = (enc.has_bom && data.size() >= 3) ? 3 : 0;
            return std::string(reinterpret_cast<const char*>(data.data() + start),
                               data.size() - start);
        }
        case Encoding::TCVN3:       return tcvn3_to_utf8(data);
        case Encoding::VNI:         return vni_to_utf8(data);
        case Encoding::Windows1258: return win1258_to_utf8(data);
        default:
            return std::unexpected("Unsupported encoding: " + enc.charset_name);
    }
}

